function transform(extra = {}) {
  return {
    ty: "tr",
    p: { a: 0, k: [0, 0] },
    a: { a: 0, k: [0, 0] },
    s: { a: 0, k: [100, 100] },
    r: { a: 0, k: 0 },
    o: { a: 0, k: 100 },
    ...extra,
  };
}

function shapeGroup(items) {
  return [{ ty: "gr", it: [...items, transform()] }];
}

function circle(fill, size = 12) {
  return [
    { ty: "el", p: { a: 0, k: [0, 0] }, s: { a: 0, k: [size, size] } },
    { ty: "fl", c: { a: 0, k: fill }, o: { a: 0, k: 100 }, r: 1, bm: 0 },
  ];
}

function rect(w, h, fill, r = 2) {
  return [
    { ty: "rc", p: { a: 0, k: [0, 0] }, s: { a: 0, k: [w, h] }, r: { a: 0, k: r } },
    { ty: "fl", c: { a: 0, k: fill }, o: { a: 0, k: 100 }, r: 1, bm: 0 },
  ];
}

function layer(name, shapeItems, ksExtra = {}) {
  return {
    ddd: 0,
    ind: 1,
    ty: 4,
    nm: name,
    sr: 1,
    ks: {
      o: { a: 0, k: 100 },
      r: { a: 0, k: 0 },
      p: { a: 0, k: [60, 60, 0] },
      a: { a: 0, k: [0, 0, 0] },
      s: { a: 0, k: [100, 100, 100] },
      ...ksExtra,
    },
    ao: 0,
    shapes: shapeGroup(shapeItems),
    ip: 0,
    op: 90,
    st: 0,
    bm: 0,
  };
}

